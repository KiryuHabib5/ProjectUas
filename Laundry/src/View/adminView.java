    public void tampilData(){
        table.getDataVector().removeAllElements();
        table.fireTableDataChanged();
        String sql = ("SELECT * FROM pesan");
        try {
            Statement stat = (Statement) KoneksiDatabase.getKoneksi().createStatement();
            ResultSet res = stat.executeQuery(sql);
            
            while(res.next()){
                Object[] hasil;
                hasil = new Object[8];
                hasil[0] = res.getString("id");
                hasil[1] = res.getString("username");
                hasil[2] = res.getString("estimasiBerat");
                hasil[3] = res.getString("kategori");
                hasil[4] = res.getString("jenisSabun");
                hasil[5] = res.getString("alamat");
                hasil[6] = res.getString("tanggal");
                hasil[7] = res.getString("status");
                table.addRow(hasil);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        buatKolomSesuai(daftarPesanan);
    }
    private void buatKolomSesuai(JTable table){
        TableColumnModel modelColumn = table.getColumnModel();
        for (int col = 0; col < modelColumn.getColumnCount(); col++) {
            int lebarKolomMax = 0;
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer rend = table.getCellRenderer(row, col);
                Object nilaiTable = table.getValueAt(row, col);
                Component comp = rend.getTableCellRendererComponent(table, nilaiTable, false, false, row, col);
                lebarKolomMax = Math.max(comp.getPreferredSize().width, lebarKolomMax);
            }
            TableColumn column = modelColumn.getColumn(col);
            column.setPreferredWidth(lebarKolomMax);
        }
    }
    private void ambilDataTabel(){
        int index = daftarPesanan.getSelectedRow();
        String id = String.valueOf(daftarPesanan.getValueAt(index, 0));
        String username = String.valueOf(daftarPesanan.getValueAt(index, 1));
        String estimasiBerat = String.valueOf(daftarPesanan.getValueAt(index, 2));
        String kategori = String.valueOf(daftarPesanan.getValueAt(index, 3));
        String jenisSabun = String.valueOf(daftarPesanan.getValueAt(index, 4));
        String alamat = String.valueOf(daftarPesanan.getValueAt(index, 5));
        String status = String.valueOf(daftarPesanan.getValueAt(index, 7));
        detailPesanan dP = new detailPesanan(this);
        dP.setIdDetail(id);
        dP.setUsernameDetail(username);
        dP.setEstimasiDetail(estimasiBerat);
        dP.setKategoriDetail(kategori);
        dP.setJenisSabunDetail(jenisSabun);
        dP.setAlamatDetail(alamat);
        dP.setStatusDetail(status);
        dP.setVisible(true);
    } 
    
